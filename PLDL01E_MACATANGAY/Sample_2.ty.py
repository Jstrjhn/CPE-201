# initialization for the value of sum & product
sum = 0
product = 0
# getting inputs for 1st number & 2nd number value
first_number = int(input("Enter the value of the 1st number:"))
second_number = int(input("Enter the value of the 2nd number:"))
# setting formula for the computation of sum & product
sum = first_number + second_number
product = first_number * second_number
# displaying the computed value for sum & product
print("The sum of two numbers is:",sum)
print("The product of the two numbers is:",product)



